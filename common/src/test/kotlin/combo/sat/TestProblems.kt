fun mutex(size: Int) = Problem(arrayOf(Cardinality(IntArray(size) { it }.applyTransform { it.asLiteral(true) })), size)
fun open(size: Int) = Problem(arrayOf(), size)

fun generate(nbrVariables: Int, nbrClauses: Int, seed: Long): Problem {
    val r = Rng(seed)
    val clauses = Array(nbrClauses) { _ ->
        val p = IntPermutation(nbrVariables, r)
        val nbrLit = r.binomial(0.7, nbrVariables) + 1
        val literals = IntArray(nbrLit) { id -> p.encode(id).asLiteral(r.boolean()) }.apply { sort() }
        var clause = if (r.double() < 0.80) {
            Disjunction(literals)
        } else if (r.boolean()) {
            literals.applyTransform { it2 -> if (!it2.asBoolean()) !it2 else it2 }
            Cardinality(literals, min(1, r.binomial(0.2, r.int(literals.size))), Cardinality.Operator.values()[r.int(3)])
        } else if (r.boolean()) {
            val clauseLits = literals.sliceArray(1 until literals.size)
            val c = if (r.boolean()) Conjunction(clauseLits) else Disjunction(clauseLits)
            Reified(literals[0], c)
        } else {
            Conjunction(literals.sliceArray(0 until min(3, r.int(literals.size))))
        }.apply { validate() }
        if (r.double() < 0.1 && clause is Clause && literals.size < nbrVariables) {
            val choices = (0 until nbrVariables).asIterable() - literals.map { it.asIx() }.toList()
            val literal = choices[r.int(choices.size)].asLiteral(r.boolean())
            clause = Reified(literal, clause)
            clause.validate()
        }
        clause
    }
    return Problem(clauses, nbrVariables)
}

